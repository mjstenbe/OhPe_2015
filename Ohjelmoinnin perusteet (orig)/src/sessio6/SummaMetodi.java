package sessio6;

public class SummaMetodi {

	public static void main(String[] args) {
		Laske(5,3);
		Jaa(5,3);
	}

	public static void Laske(int eka, int toka) {
		System.out.println(eka+toka);
	}
	
	public static void Jaa(double eka, double toka) {
		System.out.println(eka/toka);
	}
}
