package Sessio4;

public class Whilet {

	public static void main(String[] args) {

		// Alkuehtoinen while
		int i = 1;
		while (i < 11) {
			System.out.println(i); // i++ lisää i:n arvoa yhdellä
			i++;
		}

		// loppuehtoinen while
		
		int lukuMaara = 0;
		do {
			System.out.println("Hei maailma!");
			lukuMaara++; // ++ lisää ykkösellä
		} while (lukuMaara < 2);

	}
}





